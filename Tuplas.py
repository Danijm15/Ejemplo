# TUPLAS
# Las tuplas son una colección de elementos ordenados al igual que las listas, pero las tuplas son inmutables.
# Inmutables = no se pueden modificar, ni borrar ni añadir.

coordenadas = (55.17, 45.32, "Hola")
print(len(coordenadas))
lista = [55.17, 45.32, "Hola"]
lista[0] = 5
print(lista)
coordenadas[0] = 5
print(coordenadas)